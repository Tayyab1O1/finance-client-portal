  logoImageUrl?: string;
  servicesAvailed?: string[];
  clientLinks?: ClientLink[];
  formsEnabled?: boolean;
  forms?: ClientForm[];
  apCalendar?: ApCalendar;
  dashboardsEnabled?: Partial<Record<DashboardType, boolean>>;
