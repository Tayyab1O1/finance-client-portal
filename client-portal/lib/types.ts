import { Timestamp } from "firebase/firestore";

export interface ClientLink {
  id: string;
  label: string;
  linkText: string;
  url: string;
}

export interface ClientForm {
  id: string;
  label: string;
  url: string;
}

export type ApRecurrence = "weekly" | "biweekly" | "monthly";

export interface ApCalendar {
  startDate: string; // ISO date, e.g. "2026-01-05"
  recurrence: ApRecurrence;
  // Dates generated from startDate+recurrence but excluded (e.g. holidays), ISO date strings
  skipDates: string[];
  // One-off dates added outside the recurrence pattern, ISO date strings
  extraDates: string[];
  // Rolling-window generated dates, ISO date strings — regenerated by the top-up job,
  // never rewritten once a date has been assigned to a transaction row
  generatedDates: string[];
}

export type DashboardType = "payable" | "receivable";

// Maps a Fillout form to the client/dashboard it feeds. Created and deleted
// entirely through the admin "Connect Fillout Form" action — never hand-edited,
// never hardcoded — so onboarding a new client's form needs no code change.
export interface FilloutFormMapping {
  filloutFormId: string; // doc id
  clientId: string;
  dashboardType: DashboardType;
  label: string;
  webhookId: string; // Fillout's webhook id, needed to disconnect later
  createdAt: Timestamp;
}

